//Write a Java program by using two for loops to produce the output shown below:

class producePattern 
{
	public static void main(String[] args) 
	{
		printStars();
	}


	public static void printStars(){
		int i,j;
		for(i=0;i<=50;i++){
			for(j=0;j<=50-i;j++){
				System.out.print(".");

			}
			System.out.println("");

		
		}
	
	}
}

